namespace tool_monitoring\privacy;

use advanced_testcase;
use core_privacy\local\metadata\null_provider;
use PHPUnit\Framework\Attributes\CoversClass;

/**
 * Unit tests for the {@see provider} class.
 *
 * @package    tool_monitoring
 * @copyright  2025 MootDACH DevCamp
 *             Daniel Fainberg <d.fainberg@tu-berlin.de>
 *             Martin Gauk <martin.gauk@tu-berlin.de>
 *             Sebastian Rupp <sr@artcodix.com>
 *             Malte Schmitz <mal.schmitz@uni-luebeck.de>
 *             Melanie Treitinger <melanie.treitinger@ruhr-uni-bochum.de>
 * @license    https://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
#[CoversClass(provider::class)]
final class provider_test extends advanced_testcase {
    public function test_get_reason(): void {
        self::assertTrue(is_subclass_of(provider::class, null_provider::class));
        $stringid = provider::get_reason();
        self::assertTrue(get_string_manager()->string_exists($stringid, 'tool_monitoring'));
    }
}
